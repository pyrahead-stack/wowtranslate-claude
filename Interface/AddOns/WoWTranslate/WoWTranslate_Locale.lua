-- ============================================================================
-- LANGUAGE SELECTION
-- ============================================================================
-- Map a WoW client locale (GetLocale()) to one of our UI language codes.
local CLIENT_LOCALE_MAP = {
    enUS = "en", enGB = "en",
    deDE = "de",
    frFR = "fr",
    esES = "es", esMX = "es",
    ptBR = "pt", ptPT = "pt",
    itIT = "it",
    ruRU = "ru",
    zhCN = "zh", zhTW = "zh",
    koKR = "ko",
    jaJP = "ja",
}

function WoWTranslate_DetectUILanguage()
    local loc = (GetLocale and GetLocale()) or "enUS"
    return CLIENT_LOCALE_MAP[loc] or "en"
end

-- Refill WoWTranslate_L in place: English base first, then overlay the chosen
-- language. Keeps the same table object so captured `local L` references stay valid.
function WoWTranslate_SetUILanguage(code)
    if code == "auto" or code == nil then
        code = WoWTranslate_DetectUILanguage()
    end
    if not locales[code] then code = "en" end

    for k in pairs(WoWTranslate_L) do WoWTranslate_L[k] = nil end
    for k, v in pairs(enUS) do WoWTranslate_L[k] = v end
    if code ~= "en" then
        for k, v in pairs(locales[code]) do WoWTranslate_L[k] = v end
    end
    WoWTranslate_ActiveUILang = code
    return code
end

function WoWTranslate_GetUILanguages()
    return { "en", "de", "fr", "es", "pt", "it", "ru", "zh", "ko", "ja" }
end

-- Apply at load time. WoWTranslateDB is loaded before addon files in 1.12, so the
-- saved /wt lang override (if any) is available here; otherwise we auto-detect.
local saved = WoWTranslateDB and WoWTranslateDB.uiLang
WoWTranslate_SetUILanguage(saved or "auto")
