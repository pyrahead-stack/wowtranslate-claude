-- WoWTranslate_Locale.lua
-- Interface localization for WoWTranslate.
--
-- Loads FIRST (see .toc) because the config panel and minimap menu build their
-- text at file-load time, so WoWTranslate_L must already be populated.
--
-- How it works:
--   * WoWTranslate_L is a single stable table. Code captures `local L = WoWTranslate_L`
--     once; we never replace the table, we refill it in place so those references
--     keep seeing the current language.
--   * The English table below holds EVERY key. Other languages only override the
--     keys they translate; anything missing falls back to English automatically.
--   * The active language is the /wt lang override (WoWTranslateDB.uiLang) if set,
--     otherwise auto-detected from the WoW client locale (GetLocale()).
--
-- NOTE: language *names* (Chinese, German, ...) stay English on purpose so they
-- render on every client font. Test/debug command output is also left English.

WoWTranslate_L = WoWTranslate_L or {}

